# 【仕様】
# 「ファイル名を入力してください:」と聞く。
# ls コマンドでそのファイルを表示しようとする（ls 入力された名前）。
# もしファイルが存在して成功したら「ファイルあったよ！」と表示。
# もし存在しなくて失敗したら「そんなファイルないよ！」と表示。
# ※ ls のエラーメッセージが出ても気にしなくていいけど、消せたらカッコいい（2> /dev/null）。

#!/bin/bash
echo "ファイル名を入力してください:" 
read filename

ls "$filename" > /dev/null 2> /dev/null

if [ $? -ne 0 ]; then
    echo "そんなファイルないよ！"
else
    echo "ファイルあったよ！"
fi